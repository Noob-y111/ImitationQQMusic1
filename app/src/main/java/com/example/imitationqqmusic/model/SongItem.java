package com.example.imitationqqmusic.model;

public class SongItem {

    private String name;
    private String singer;

    public SongItem(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSinger() {
        return singer;
    }

    public void setSinger(String singer) {
        this.singer = singer;
    }
}
